char original[81]; // Исходная строка (максимум 80 символов + 1 для '\0')
char trimmed[81];  // Новая строка (максимум 80 символов + 1 для '\0')

cout << "Введите строку (максимум 80 символов): ";
cin.getline(original, 81); // Считываем строку

char* start = original; // Указатель на начало строки
char* end = original;   // Указатель для нахождения конца строки

// Находим первый незначащий пробел
while (*start == ' ') {
    start++;
}

// Находим конец строки и игнорируем незначащие пробелы в конце
while (*end != '\0') {
    end++;
}
end--; // Сдвигаем указатель на последний символ

while (end > start && *end == ' ') {
    end--;
}

// Копируем строку, заменяя множественные пробелы на один
char* dest = trimmed;
bool spaceFound = false;

while (start <= end) {
    if (*start != ' ') {
        *dest++ = *start; // Копируем символ, если это не пробел
        spaceFound = false; // Сбрасываем флаг
    }
    else if (!spaceFound) {
        *dest++ = ' '; // Копируем один пробел
        spaceFound = true; // Устанавливаем флаг, что пробел найден
    }
    start++;
}

*dest = '\0'; // Завершаем новую строку нулевым символом

// Выводим исходную и новую строки
cout << "Исходная строка: '" << original << "'" << endl;
cout << "Новая строка: '" << trimmed << "'" << endl;

